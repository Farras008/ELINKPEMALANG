public class CagarModel {
    private String judulCagar, deskripsiCagar;
    private int logoCagar;

    public String getJudulCagar() {
        return judulCagar;
    }

    public void setJudulCagar(String judulCagar) {
        this.judulCagar = judulCagar;
    }

    public String getDeskripsiCagar() {
        return deskripsiCagar;
    }

    public void setDeskripsiCagar(String deskripsiCagar) {
        this.deskripsiCagar = deskripsiCagar;
    }

    public int getLogoCagar() {
        return logoCagar;
    }

    public void setLogoCagar(int logoCagar) {
        this.logoCagar = logoCagar;
    }
}
